<template><div><h1 id="视野中心" tabindex="-1"><a class="header-anchor" href="#视野中心" aria-hidden="true">#</a> 视野中心</h1>
<h2 id="定义" tabindex="-1"><a class="header-anchor" href="#定义" aria-hidden="true">#</a> 定义</h2>
<p><strong>视野中心</strong>，即当前3D视图的中心点</p>
<p>在旋转视图时，视图围绕着视图中心旋转</p>
<p>在Unity中，不支持绕着某个物体旋转视图</p>
<h2 id="框选所选" tabindex="-1"><a class="header-anchor" href="#框选所选" aria-hidden="true">#</a> 框选所选</h2>
<ul>
<li>选中一个物体</li>
<li>按F键，将物体置于视野中心</li>
</ul>
<h2 id="其他" tabindex="-1"><a class="header-anchor" href="#其他" aria-hidden="true">#</a> 其他</h2>
<p>出生点：添加一个新物体，物体位于视图中心</p>
<p><strong>注意：出生点不在(0,0,0)，而在当前视图中心</strong></p>
